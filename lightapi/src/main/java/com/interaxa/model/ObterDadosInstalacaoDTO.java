package com.interaxa.model;

public class ObterDadosInstalacaoDTO {
    private String anlage;
    private String vertrag;
    private String vkont;
    private String partner;
    private String url;
    private String destino;
 
    public ObterDadosInstalacaoDTO() {
    }
 
    public String getAnlage() {
       return this.anlage;
    }
 
    public void setAnlage(String anlage) {
       this.anlage = anlage;
    }
 
    public String getVertrag() {
       return this.vertrag;
    }
 
    public void setVertrag(String vertrag) {
       this.vertrag = vertrag;
    }
 
    public String getVkont() {
       return this.vkont;
    }
 
    public void setVkont(String vkont) {
       this.vkont = vkont;
    }
 
    public String getPartner() {
       return this.partner;
    }
 
    public void setPartner(String partner) {
       this.partner = partner;
    }
 
    public String getUrl() {
       return this.url;
    }
 
    public void setUrl(String url) {
       this.url = url;
    }
 
    public String getDestino() {
       return this.destino;
    }
 
    public void setDestino(String destino) {
       this.destino = destino;
    }
}
